CREATE OR REPLACE FUNCTION "public"."get_user"("user_id_param" "uuid") RETURNS TABLE("id" bigint, "name" "text", "email" "text", "profile_path" "text", "user_id" "uuid", "device_id" "text", "total_token_used" bigint, "is_premium_user" boolean)
    LANGUAGE "plpgsql"
    AS $$
BEGIN
  RETURN QUERY
  SELECT
    users.id,
    users.name,
    users.email,
    users.profile_path,
    users.user_id,
    users.device_id,
    users.total_token_used,
    CASE
      WHEN users.expired_at < CURRENT_DATE THEN false
      ELSE true
    END AS is_premium_user
  FROM
    users
  WHERE users.user_id = user_id_param;
END;
$$;


