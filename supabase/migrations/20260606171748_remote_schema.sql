CREATE OR REPLACE FUNCTION "public"."get_translations_with_vocabularies_by_day"("translation_day_id_param" integer) RETURNS TABLE("id" bigint, "burmese_text" "text", "english_text" "text", "words" "text", "day_id" bigint, "vocabularies" "jsonb")
    LANGUAGE "plpgsql"
    AS $$
BEGIN
  RETURN QUERY
  SELECT 
    t.id,
    t.burmese_text,
    t.english_text,
    t.words,
    t.translation_day_id,
    COALESCE(
      JSON_AGG(
        JSON_BUILD_OBJECT(
          'id', pv.id,
          'english_text', pv.english_text,
          'burmese_text', pv.burmese_text,
          'audio_path', pv.audio_path
        )
      ) FILTER (WHERE pv.id IS NOT NULL)::JSONB,
      '[]'::JSONB
    ) AS vocabularies
  FROM 
    translations t
  LEFT JOIN 
    translation_vocabulary_relation tvr ON t.id = tvr.translation_id
  LEFT JOIN 
    pattern_vocabularies pv ON tvr.vocabulary_id = pv.id
  WHERE 
    t.translation_day_id = translation_day_id_param
  GROUP BY 
    t.id, t.burmese_text, t.english_text
  ORDER BY 
    t.id;
END;
$$;


