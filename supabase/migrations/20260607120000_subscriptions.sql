-- Subscriptions & plans (manual admin activation)
-- Free vs premium is derived from users.premium_until (denormalized cache).
-- Source of truth = public.subscriptions (one row per manual grant).

-- 1. Rename the legacy premium cache column for clarity.
--    get_user already drives is_premium_user off this column; mobile only ever
--    reads the derived boolean, so renaming is safe.
ALTER TABLE "public"."users" RENAME COLUMN "expired_at" TO "premium_until";

-- 2. Plan catalog (managed from the admin; prices in minor units, e.g. MMK).
CREATE TABLE IF NOT EXISTS "public"."subscription_plans" (
    "id" bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "code" text NOT NULL UNIQUE,                       -- 'monthly' | '6_month' | '12_month'
    "name" text NOT NULL,
    "duration_days" integer NOT NULL,
    "price_cents" integer NOT NULL DEFAULT 0,
    "currency" text NOT NULL DEFAULT 'MMK',
    "daily_token_grant" integer NOT NULL DEFAULT 72000,   -- credit-entitlement: premium grant
    "max_recording_seconds" integer NOT NULL DEFAULT 300, -- 5 min premium
    "is_active" boolean NOT NULL DEFAULT true,
    "created_at" timestamp with time zone NOT NULL DEFAULT now()
);

ALTER TABLE "public"."subscription_plans" OWNER TO "postgres";

-- 3. Subscription ledger. One row per grant; doubles as purchase history.
CREATE TABLE IF NOT EXISTS "public"."subscriptions" (
    "id" bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "user_id" bigint NOT NULL REFERENCES "public"."users"("id") ON UPDATE CASCADE ON DELETE CASCADE,
    "plan_id" bigint NOT NULL REFERENCES "public"."subscription_plans"("id") ON UPDATE CASCADE ON DELETE RESTRICT,
    "status" text NOT NULL DEFAULT 'active',          -- active | expired | refunded | trialing
    "started_at" timestamp with time zone NOT NULL DEFAULT now(),
    "current_period_end" timestamp with time zone NOT NULL,
    "provider" text NOT NULL DEFAULT 'manual',        -- manual | play | apple | stripe (future)
    "payment_ref" text,                               -- bank / KBZPay / Wave reference
    "payment_proof_path" text,                        -- optional screenshot (private bucket)
    "granted_by" uuid,                                -- admin auth.uid() when available
    "note" text,
    "created_at" timestamp with time zone NOT NULL DEFAULT now()
);

ALTER TABLE "public"."subscriptions" OWNER TO "postgres";

CREATE INDEX IF NOT EXISTS "subscriptions_user_id_idx" ON "public"."subscriptions" ("user_id");
CREATE INDEX IF NOT EXISTS "subscriptions_status_idx" ON "public"."subscriptions" ("status");

